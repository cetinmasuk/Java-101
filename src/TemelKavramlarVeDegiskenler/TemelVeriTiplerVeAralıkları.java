package TemelKavramlarVeDegiskenler;



public class TemelVeriTiplerVeAralıkları {
    public static void main(String[] args) {
         /*byte> short>int>long>float>double
          ->Float, virgülden sonra 7 basamak,
          ->Double, virgülden sonra 15 basamak sağlar.
          */

        //Tamsayı değerler.
        System.out.println("byten max. değeri: " + Byte.MAX_VALUE);
        System.out.println("byten min. değeri: " + Byte.MIN_VALUE);
        System.out.println(" *** \n ");
        System.out.println("short max. değeri: " + Short.MAX_VALUE);
        System.out.println("short min. değeri: " + Short.MIN_VALUE);
        System.out.println(" *** \n ");
        System.out.println("int max. değeri: " +Integer.MAX_VALUE);
        System.out.println("int min değeri: "+Integer.MIN_VALUE);
        System.out.println(" *** \n ");

        //Ondalık değerler.
        System.out.println("double max. değeri: " + Double.MAX_VALUE);
        System.out.println("double min. değeri: " + Double.MIN_VALUE);
        System.out.println(" *** \n ");
        System.out.println("float max. değeri: " + Float.MAX_VALUE);
        System.out.println("float min. değeri: "+ Float.MIN_VALUE);
        System.out.println("float min. değeri: "+ Float.MIN_VALUE);
        System.out.println("float min. değeri: "+ Float.MIN_VALUE);

        //Float ve double kullanılurken sonuna f veya F yada d veya D eklenmelidir.





    }
}
